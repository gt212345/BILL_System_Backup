package com.usc.bill.model;

/**
 * To provide unique values of class status for students
 * @author WU, Alamri, STROUD
 * @version 1.0 11/19/2017
 */ 
public enum ClassStatus
{
	/**
	* indicates freshman class status
	*/ 
	FRESHMAN, 
	
	/**
	* indicates sophomore class status
	*/ 
	SOPHOMORE,
	
	/**
	* indicates junior class status
	*/ 
	JUNIOR, 
	/**
	* indicates senior class status
	*/ 
	SENIOR,
	
	/**
	* indicates masters class status
	*/ 
	MASTERS,
	
	/**
	* indicates PhD class status
	*/ 
	PHD,
	
	/**
	* indicates graduated class status
	*/ 	
	GRADUATED;
}
