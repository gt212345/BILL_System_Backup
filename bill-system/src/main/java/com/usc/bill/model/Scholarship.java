package com.usc.bill.model;

/**
 * To provide unique values of scholarship types for students
 * @author WU, Alamri, STROUD
 * @version 1.0 11/19/2017
 */ 
public enum Scholarship
{
	/**
	* indicates general scholarship
	*/ 
	GENERAL, 
	
	/**
	* indicates athletic scholarship
	*/ 
	ATHLETIC,

	/**
	* indicates departmental scholarship
	*/ 	
	DEPARTMENTAL,

	/**
	* indicates woodrow scholarship
	*/ 	
	WOODROW,

	/**
	* indicates sims scholarship
	*/ 
	SIMS, 
	
	/**
	* indicates no scholarship
	*/ 
	NONE;
}
